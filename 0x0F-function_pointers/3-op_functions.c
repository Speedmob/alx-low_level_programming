#include "3-calc.h"
/**
 * op_add - adds two int
 * @a: first int
 * @b: second int
 *
 * Return: a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two int
 * @a: first int
 * @b: second int
 *
 * Return: a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul two int
 * @a: first int
 * @b: second int
 *
 * Return: a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div two int
 * @a: first int
 * @b: second int
 *
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod two int
 * @a: first int
 * @b: second int
 *
 * Return: a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
